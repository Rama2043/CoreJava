public class datatypeexample {
    public static void main(String[] args) {
        int a = 2;
        double b = 3.0;
        char c = 'A';
        boolean d = true;
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("c="+c);
        System.out.println("d="+d);

    }
}
