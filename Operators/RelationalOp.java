package Operators;

public class RelationalOp {
    public static void main(String[] args) {
        int a = 10, b = 5;
        System.out.println("Output:" + (a==b) + " (Equal to)");
        System.out.println("Output:" + (a!=b) + " (Not equal to)");
        System.out.println("Output:" + (a>b) + " (Greater than)");
        System.out.println("Output:" + (a<b) + " (Less than)");
        System.out.println("Output:" + (a>=b) + " (Greater than or equal to)");
        System.out.println("Output:" + (a<=b) + " (Less than or equal to)");
    }
}
