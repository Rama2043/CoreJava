package Operators;

public class ArithmeticOp {
    public static void main(String[] args) {
        int a = 10, b = 5;
        System.out.println("Result:"+(a+b));
        System.out.println("Result:"+(a-b));
        System.out.println("Result:"+(a*b));
        System.out.println("Result:"+(a/b));
        System.out.println("Result:"+(a%b));

    }
}
