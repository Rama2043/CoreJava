package Operators;

public class AssignmentOp {
    public static void main(String[] args) {
        int a = 5;
        a += 3;
        System.out.println("Output:"+a);
        a -= 2;
        System.out.println("Output:"+a);
        a *= 2;
        System.out.println("Output:"+a);
        a /= 3;
        System.out.println("Output:"+a);
        a %= 3;
        System.out.println("Output:"+a);
    }

}
